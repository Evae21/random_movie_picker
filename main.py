import random

MOVIES = [
    "Інтерстеллар", "Початок", "Матриця", "Бійцівський клуб",
    "Паразити", "Оппенгеймер", "Дюна: Частина друга", "1917",
    "Все скрізь і одразу", "Володар перснів: Братство персня",
    "Сьомий", "Втеча з Шоушенка", "Зеленая миля", "Форрест Гамп"
]

def pick_movie():
    print("Сьогодні ввечері дивимось...")
    print("✨" * 12)
    movie = random.choice(MOVIES)
    print(f"   ★  {movie}  ★")
    print("✨" * 12)
    
    # Додатковий "драматичний" ефект
    print("\n(підбадьорювально) Готуйся, буде вогонь! 🔥")


if __name__ == "__main__":
    input("Натисни Enter, щоб дізнатись, що дивитись сьогодні...\n")
    pick_movie()
